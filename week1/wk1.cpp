#include <iostream>
#include <bits/stdc++.h>
using namespace std;

double distance(double x1, double y1, double x2, double y2)
{
    double deltaX = x2 - x1;
    double deltaY = y2 - y2;
    double result;
    result = pow(deltaX, 2) + pow(deltaY, 2);
    result = pow(result, 0.5);
}
